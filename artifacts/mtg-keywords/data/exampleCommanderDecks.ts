export function exampleDeckToCards(deck: ExampleCommanderDeck) {
  return deck.cards.map(c => ({
    id: c.name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, ""),
    name: c.name,
    type_line: c.type_line,
    mana_cost: c.mana_cost,
    count: c.count,
  }));
}

export function totalCardCount(deck: ExampleCommanderDeck): number {
  const spells = deck.cards.reduce((s, c) => s + c.count, 0);
  const basics  = (deck.lands.W + deck.lands.U + deck.lands.B + deck.lands.R + deck.lands.G);
  return spells + basics;
}
